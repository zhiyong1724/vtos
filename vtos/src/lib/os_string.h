#ifndef _OS_STRIN_H
#define _OS_STRIN_H
#include "os_cpu.h"
/*********************************************************************************************************************
 * 该函数可以设置一段内存空间的值
 * s：指向要被设置的内存空间
 * ch：需要被设置的值
 * n：要设置的大小
 * return：返回指向要被设置的内存空间
 *********************************************************************************************************************/
void *os_mem_set(void *s, int8 ch, os_size_t n);
/*********************************************************************************************************************
 * 字符串拷贝函数
 * dest：指向被拷贝字符串的存放空间
 * src：指向被拷贝字符串
 * n：最大的拷贝大小
 * return：返回指向被拷贝字符串空间
 *********************************************************************************************************************/
void *os_str_cpy(char *dest, const char *src, os_size_t n);
/*********************************************************************************************************************
 * 把一个无符号整数转换成字符串
 * buff：保存转换后的字符串
 * num：要转换的无符号整数
 *********************************************************************************************************************/
void os_utoa(char *buff, os_size_t num);
#endif
